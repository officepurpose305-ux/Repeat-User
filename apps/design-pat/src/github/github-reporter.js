import axios from 'axios';
import fs from 'fs/promises';
import path from 'path';

/**
 * Post design comparison results as GitHub PR comment
 */
export async function postGitHubComment(report, config) {
  const {
    githubToken,
    githubRepo, // owner/repo
    githubPrNumber,
    outputDir,
  } = config;

  if (!githubToken || !githubRepo || !githubPrNumber) {
    console.log('⚠️  GitHub integration not configured. Skipping GitHub comment.');
    return;
  }

  try {
    console.log('📤 Posting results to GitHub PR...');

    // Build markdown comment
    const comment = buildGitHubComment(report, config);

    // Post to GitHub API
    const [owner, repo] = githubRepo.split('/');
    const url = `https://api.github.com/repos/${owner}/${repo}/issues/${githubPrNumber}/comments`;

    const response = await axios.post(
      url,
      { body: comment },
      {
        headers: {
          Authorization: `token ${githubToken}`,
          Accept: 'application/vnd.github.v3+json',
        },
      }
    );

    console.log(`✅ Posted comment: ${response.data.html_url}`);
    return response.data;
  } catch (error) {
    console.error(`❌ Failed to post GitHub comment: ${error.message}`);
    throw error;
  }
}

/**
 * Build GitHub markdown comment with results
 */
function buildGitHubComment(report, config) {
  const accuracy = report.accuracy.toFixed(1);
  const status = report.passed ? '✅ PASSED' : '❌ FAILED';
  const threshold = config.ciThreshold || 95;

  // Build issues section
  let issuesSection = '';
  if (report.allIssues && report.allIssues.length > 0) {
    const highIssues = report.allIssues.filter((i) => i.severity === 'high');
    const mediumIssues = report.allIssues.filter((i) => i.severity === 'medium');
    const lowIssues = report.allIssues.filter((i) => i.severity === 'low');

    issuesSection = `
## 🔍 Issues Found (${report.allIssues.length} total)

${
  highIssues.length > 0
    ? `### 🔴 High Priority (${highIssues.length})\n${highIssues
        .map((i) => formatIssue(i))
        .join('\n')}`
    : ''
}

${
  mediumIssues.length > 0
    ? `### 🟠 Medium Priority (${mediumIssues.length})\n${mediumIssues
        .map((i) => formatIssue(i))
        .join('\n')}`
    : ''
}

${
  lowIssues.length > 0
    ? `### 🔵 Low Priority (${lowIssues.length})\n${lowIssues
        .map((i) => formatIssue(i))
        .join('\n')}`
    : ''
}
`;
  } else {
    issuesSection = '### ✨ No issues found!';
  }

  return `## 🎨 Design vs Build Comparison

**Status:** ${status}
**Accuracy:** ${accuracy}% ${accuracy >= threshold ? '✅' : '⚠️ Below ' + threshold + '% threshold'}
**Screen:** ${report.screenName}

---

${issuesSection}

---

### 📊 Summary
- **Figma frames compared:** 1
- **Screenshots analyzed:** 1
- **Pixel match:** ${accuracy}%
- **Mismatch pixels:** ${report.mismatchPixels?.toLocaleString() || '0'} / ${
    report.totalPixels?.toLocaleString() || '0'
  }

**View full report:** Run \`npm run dashboard\` to see annotated screenshots and interactive comparison.

---

*Generated by [Design PAT](https://github.com/anthropics/claude-code) 🤖*
`;
}

/**
 * Format single issue for GitHub markdown
 */
function formatIssue(issue) {
  return `
<details>
<summary><strong>${issue.type}</strong> — ${issue.element}</summary>

**Problem:** ${issue.description}

\`\`\`css
${issue.cssFix}
\`\`\`

</details>
`;
}
